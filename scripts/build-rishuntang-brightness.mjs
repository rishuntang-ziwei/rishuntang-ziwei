/** @param {Record<string, Record<string, [string, string][]>>} ref */
function refToJson(ref) {
  /** @type {Record<string, Record<string, Array<{ name: string; brightness: string }>>>} */
  const out = {}
  for (const [ziweiBranch, palaces] of Object.entries(ref)) {
    out[ziweiBranch] = {}
    for (const [palaceBranch, stars] of Object.entries(palaces)) {
      out[ziweiBranch][palaceBranch] = stars.map(([name, brightness]) => ({ name, brightness }))
    }
  }
  return out
}

